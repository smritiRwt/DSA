
public class Number11 {

    public static void main(String[] args) {
        int n = 5;
        for (int i = 1; i <= n; i++) {
            int number = 1;
            for (int j = 1; j <= n; j++) {
             
                System.out.print(number);
               number++;
            }
            System.out.println();
        }
    }
}
